public class Paciente {

    //Criando classe de Paciente para cadastrado de pacientes
    private String nome;
    private String cpf;
    private String email;
    private int idade;

    public Paciente(String nome, String cpf, String email, int idade) {
        this.nome = nome;
        this.cpf = cpf;
        this.email = email;
        this.idade = idade;
    }
}
